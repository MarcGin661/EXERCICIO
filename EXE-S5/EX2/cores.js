
let cor = "blue";

switch (cor) {
  case "red":
    console.log("Cor em português: vermelho");
    break;
  case "blue":
    console.log("Cor em português: azul");
    break;
  case "green":
    console.log("Cor em português: verde");
    break;
  case "yellow":
    console.log("Cor em português: amarelo");
    break;
  case "purple":
    console.log("Cor em português: roxo");
    break;
  default:
    console.log("Cor não encontrada");
    break;
}